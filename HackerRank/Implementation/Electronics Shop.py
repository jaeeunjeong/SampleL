def getMoneySpent(keyboards, drives, b):
    #
    # Write your code here.
    #
    total_price = -1
    for keyboard_price in keyboards :
        #keyboard_price = keyboards[i]
        for drive_price in drives :
            #drive_price = drives[j]
            if (keyboard_price + drive_price) <= b :
                total_price = max(total_price ,(keyboard_price + drive_price))
    return total_price


